package com.stn.survbar.config;

/**
 * Configuration for STN Survival Bar HUD positioning.
 */
public class STNSurvbarConfig {

    // HUD position offsets
    public static int xOffset = 89;
    public static int yOffset = 31;

    public static void setXOffset(int x) {
        xOffset = x;
    }

    public static void setYOffset(int y) {
        yOffset = y;
    }
}
